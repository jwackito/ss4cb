/**
 * يُجري بحثًا بالعرض أولًا على رسم بياني ممثل كقائمة مجاور.
 * يُعيد ترتيب زيارة العقد بدءًا من تاريخ معين.
 */
function بحث_بتفضيل_التوسع(قائمة_التجاور, الجذر) {
    const الْعُقَد_تمت_زيارتها = new Set();
    const الطابور = [];
    const ترتيب_الزيارة = [];

    الْعُقَد_تمت_زيارتها.add(الجذر);
    الطابور.push(الجذر);

    while (الطابور.length > 0) {
        const العقدة_الحالية = الطابور.shift();
        ترتيب_الزيارة.push(العقدة_الحالية);

        const مجاور_الْعُقَد = قائمة_التجاور[العقدة_الحالية] || [];
        for (const العقدة_المجاورة of مجاور_الْعُقَد) {
            if (!الْعُقَد_تمت_زيارتها.has(العقدة_المجاورة)) {
                الْعُقَد_تمت_زيارتها.add(العقدة_المجاورة);
                الطابور.push(العقدة_المجاورة);
            }
        }
    }

    return ترتيب_الزيارة;
}

const مثال_بيان = {
    0: [1, 2],
    1: [0, 3, 4],
    2: [0, 5],
    3: [1],
    4: [1],
    5: [2]
};

const نتيجة = بحث_بتفضيل_التوسع(مثال_بيان, 0);
console.log("BFS الزيارة ترتيب:", نتيجة);
