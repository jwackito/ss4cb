/**
 * एडजेंसी लिस्ट के तौर पर दिखाए गए ग्राफ़ पर ब्रेडथ-फर्स्ट सर्च करता है.
 * स्टार्ट_नोड से शुरू करके नोड्स को विज़िट करने का ऑर्डर बताता है.
 */
function विस्तार_प्रधान_खोज(पड़ोसी_सूची , स्टार्ट_नोड ) {
    const देखे_गए_नोड्स = new Set();
    const पंक्ति = [];
    const विज़िट_क्रम = [];

    देखे_गए_नोड्स.add(स्टार्ट_नोड );
    पंक्ति.push(स्टार्ट_नोड );

    while (पंक्ति.length > 0) {
        const वर्तमान_नोड = पंक्ति.shift();
        विज़िट_क्रम.push(वर्तमान_नोड);

        const पड़ोसी_नोड्स = पड़ोसी_सूची [वर्तमान_नोड] || [];
        for (const पड़ोसी_नोड of पड़ोसी_नोड्स) {
            if (!देखे_गए_नोड्स.has(पड़ोसी_नोड)) {
                देखे_गए_नोड्स.add(पड़ोसी_नोड);
                पंक्ति.push(पड़ोसी_नोड);
            }
        }
    }

    return विज़िट_क्रम;
}

const उदाहरण_ग्राफ = {
    0: [1, 2],
    1: [0, 3, 4],
    2: [0, 5],
    3: [1],
    4: [1],
    5: [2]
};

const परिणाम = विस्तार_प्रधान_खोज(उदाहरण_ग्राफ, 0);
console.log("BFS विज़िट क्रम:", परिणाम);
