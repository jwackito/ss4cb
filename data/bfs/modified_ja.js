/**
 * 隣接リストとして表現されたグラフに対して、幅優先探索を実行します。
 * ルートから始めて、ノードが訪問された順序を返します。
 */
function 幅優先探索(隣接リスト, 開始ノード) {
    const 訪問済みノード = new Set();
    const キュー = [];
    const 訪問順序 = [];

    訪問済みノード.add(開始ノード);
    キュー.push(開始ノード);

    while (キュー.length > 0) {
        const 現在のノード = キュー.shift();
        訪問順序.push(現在のノード);

        const 隣接ノード = 隣接リスト[現在のノード] || [];
        for (const 隣接ノード of 隣接ノード) {
            if (!訪問済みノード.has(隣接ノード)) {
                訪問済みノード.add(隣接ノード);
                キュー.push(隣接ノード);
            }
        }
    }

    return 訪問順序;
}

const 例グラフ = {
    0: [1, 2],
    1: [0, 3, 4],
    2: [0, 5],
    3: [1],
    4: [1],
    5: [2]
};

const 結果 = 幅優先探索(例グラフ, 0);
console.log("BFS に訪問順序:", 結果);
