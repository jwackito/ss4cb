/**
 * Выполняет поиск в ширину по графу, представленному в виде списка смежности.
 * Возвращает порядок посещения узлов, начиная с начальная_вершина.
 */
function Поиск_в_ширину(список_смежности, начальная_вершина) {
    const посещенные_вершины = new Set();
    const очередь = [];
    const порядок_посещения = [];

    посещенные_вершины.add(начальная_вершина);
    очередь.push(начальная_вершина);

    while (очередь.length > 0) {
        const текущая_вершина = очередь.shift();
        порядок_посещения.push(текущая_вершина);

        const соседние_вершины = список_смежности[текущая_вершина] || [];
        for (const соседа_вершин of соседние_вершины) {
            if (!посещенные_вершины.has(соседа_вершин)) {
                посещенные_вершины.add(соседа_вершин);
                очередь.push(соседа_вершин);
            }
        }
    }

    return порядок_посещения;
}

const пример_графа = {
    0: [1, 2],
    1: [0, 3, 4],
    2: [0, 5],
    3: [1],
    4: [1],
    5: [2]
};

const результат = Поиск_в_ширину(пример_графа, 0);
console.log("BFS порядок посещения:", результат);
