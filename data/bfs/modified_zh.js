/**
 * 对以邻接表表示的图执行广度优先搜索.
 * 返回从开始节点节点开始访问节点的顺序。
 */
function 广度优先搜索(邻居列表, 开始节点) {
    const 已访问节点 = new Set();
    const 队列 = [];
    const 访问顺序 = [];

    已访问节点.add(开始节点);
    队列.push(开始节点);

    while (队列.length > 0) {
        const 当前节点 = 队列.shift();
        访问顺序.push(当前节点);

        const 邻居 = 邻居列表[当前节点] || [];
        for (const 邻居节点 of 邻居) {
            if (!已访问节点.has(邻居节点)) {
                已访问节点.add(邻居节点);
                队列.push(邻居节点);
            }
        }
    }

    return 访问顺序;
}

const 例图 = {
    0: [1, 2],
    1: [0, 3, 4],
    2: [0, 5],
    3: [1],
    4: [1],
    5: [2]
};

const 结果 = 广度优先搜索(例图, 0);
console.log("BFS 访问顺序:", 结果);
