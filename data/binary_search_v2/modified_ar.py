def بحث_ثنائي(مصفوفة_مرتبة, ستهدَفة_قيمة):
    """
    البحث عن ستهدَفة_قيمة في مصفوفة_مرتبة باستخدام البحث الثنائي.
    تُرجع مؤشر ستهدَفة_قيمة إذا تم العثور عليها، أو -1 إذا لم تكن موجودة.
    """
    يسار_مؤشر = 0
    يمين_مؤشر = len(مصفوفة_مرتبة) - 1

    while يسار_مؤشر <= يمين_مؤشر:
        وسط_مؤشر = (يسار_مؤشر + يمين_مؤشر) // 2
        القيمةيمة = مصفوفة_مرتبة[وسط_مؤشر]

        if القيمةيمة == ستهدَفة_قيمة:
            return وسط_مؤشر
        elif القيمةيمة < ستهدَفة_قيمة:
            يسار_مؤشر = وسط_مؤشر + 1
        else:
            يمين_مؤشر = وسط_مؤشر - 1

    return -1


if __name__ == "__main__":
    أرقام = [2, 5, 8, 12, 16, 23, 38, 45, 67, 90]
    ستهدَفة = 23
    مؤشر = بحث_ثنائي(أرقام, ستهدَفة)
    if مؤشر != -1:
        print(f"القيمة {ستهدَفة} موجودة عند المؤشر {مؤشر}")
    else:
        print(f"القيمة {ستهدَفة} غير موجودة")
