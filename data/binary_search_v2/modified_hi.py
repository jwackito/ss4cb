def बाइनरी_खोज_प्रणाली(क्रमबद्ध_सूची , लक्ष्य_मूल्य):
    """
    बाइनरी सर्च का इस्तेमाल करके नंबर में लक्ष्य_मूल्य खोजता है।.
    अगर मिले तो लक्ष्य_मूल्य का इंडेक्स लौटाता है, या अगर मौजूद नहीं है तो -1 लौटाता है।.
    """
    बाएँ_इंडेक्स = 0
    दाएँ_इंडेक्स = len(क्रमबद्ध_सूची ) - 1

    while बाएँ_इंडेक्स <= दाएँ_इंडेक्स:
        बीच_का_इंडेक्स = (बाएँ_इंडेक्स + दाएँ_इंडेक्स) // 2
        बीच_का_मूल्य = क्रमबद्ध_सूची [बीच_का_इंडेक्स]

        if बीच_का_मूल्य == लक्ष्य_मूल्य:
            return बीच_का_इंडेक्स
        elif बीच_का_मूल्य < लक्ष्य_मूल्य:
            बाएँ_इंडेक्स = बीच_का_इंडेक्स + 1
        else:
            दाएँ_इंडेक्स = बीच_का_इंडेक्स - 1

    return -1


if __name__ == "__main__":
    नंबर_सूची = [2, 5, 8, 12, 16, 23, 38, 45, 67, 90]
    लक्ष्य = 23
    इंडेक्स = बाइनरी_खोज_प्रणाली(नंबर_सूची, लक्ष्य)
    if इंडेक्स != -1:
        print(f"मूल्य {लक्ष्य} found at इंडेक्स {इंडेक्स}")
    else:
        print(f"मूल्य {लक्ष्य} not found")
