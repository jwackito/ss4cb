def 二分探索(ソート済みリスト, ターゲット_値):
    """
    バイナリサーチを使用して、ソート済みリスト内でターゲット_値を検索します。
    ターゲット_値が見つかった場合はそのインデックスを返し、見つからない場合は-1を返します。
    """
    左インデックス = 0
    右インデックス = len(ソート済みリスト) - 1

    while 左インデックス <= 右インデックス:
        中央インデックス = (左インデックス + 右インデックス) // 2
        中央値 = ソート済みリスト[中央インデックス]

        if 中央値 == ターゲット_値:
            return 中央インデックス
        elif 中央値 < ターゲット_値:
            左インデックス = 中央インデックス + 1
        else:
            右インデックス = 中央インデックス - 1

    return -1


if __name__ == "__main__":
    数字 = [2, 5, 8, 12, 16, 23, 38, 45, 67, 90]
    ターゲット = 23
    インデックス = 二分探索(数字, ターゲット)
    if インデックス != -1:
        print(f"値 {ターゲット} がインデックス {インデックス} で見つかりました。")
    else:
        print(f"値{ターゲット}が見つかりません")
