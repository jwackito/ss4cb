def 二分搜尋(已排序列表, 目標值):
    """
    使用二分查找在已排序列表中查找目標值。
    如果找到目標值，则返回其索引；如果不存在，则返回 -1。
    """
    左边索引 = 0
    右邊索引 = len(已排序列表) - 1

    while 左边索引 <= 右邊索引:
        中间索引 = (左边索引 + 右邊索引) // 2
        中间值為 = 已排序列表[中间索引]

        if 中间值為 == 目標值:
            return 中间索引
        elif 中间值為 < 目標值:
            左边索引 = 中间索引 + 1
        else:
            右邊索引 = 中间索引 - 1

    return -1


if __name__ == "__main__":
    数字 = [2, 5, 8, 12, 16, 23, 38, 45, 67, 90]
    目标 = 23
    索引 = 二分搜尋(数字, 目标)
    if 索引 != -1:
        print(f"在索引 {索引} 处找到值 {目标}")

    else:
        print(f"未找到值 {目标}")

