#include <stdio.h>

void बबल_सोर्ट(int संख्या_एरे[], int एरे_लंबाई)
{

    /* बबल सॉर्ट का इस्तेमाल करके संख्या_एरे को बढ़ते क्रम में सॉर्ट करें */
    int आउटर_फेरा;
    int आंतरिक_फेरा;
    int अस्थायी_मूल्य;
    int स्वैप_हुआ;

    for (आउटर_फेरा = 0; आउटर_फेरा < एरे_लंबाई - 1; आउटर_फेरा++)
    {
        स्वैप_हुआ = 0;
        for (आंतरिक_फेरा = 0; आंतरिक_फेरा < एरे_लंबाई - आउटर_फेरा - 1; आंतरिक_फेरा++)
        {
            if (संख्या_एरे[आंतरिक_फेरा] > संख्या_एरे[आंतरिक_फेरा + 1])
            {
                /* आसन्न तत्वों को स्वैप करें */
                अस्थायी_मूल्य = संख्या_एरे[आंतरिक_फेरा];
                संख्या_एरे[आंतरिक_फेरा] = संख्या_एरे[आंतरिक_फेरा + 1];
                संख्या_एरे[आंतरिक_फेरा + 1] = अस्थायी_मूल्य;
                स्वैप_हुआ = 1;
            }
        }
        /* अगर इस फेरा में कोई अदला नहीं हुआ, तो अरे पहले से ही सॉर्ट हो चुका है */
        if (स्वैप_हुआ == 0)
            break;
    }
}

int main()
{
    int नंबर_सूची[] = {64, 34, 25, 12, 22, 11, 90};
    int एरे_लंबाई = 7;
    int इंडेक्स;

    बबल_सोर्ट(नंबर_सूची, एरे_लंबाई);

    printf("क्रमबद्ध एरे: ");
    for (इंडेक्स = 0; इंडेक्स < एरे_लंबाई; इंडेक्स++)
    {
        printf("%d ", नंबर_सूची[इंडेक्स]);
    }
    printf("\n");
    return 0;
}
