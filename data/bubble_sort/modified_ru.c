#include <stdio.h>

void Сортировка_пузырько́м(int числовой_массив[], int размер_массив)
{
    /* сортировать числовой_массив в порядке возрастания с помощью пузырьковой сортировки */
    int внешний_цикл;
    int внутренний_цикл;
    int временный_значение;
    int был_обмен;

    for (внешний_цикл = 0; внешний_цикл < размер_массив - 1; внешний_цикл++)
    {
        был_обмен = 0;
        for (внутренний_цикл = 0; внутренний_цикл < размер_массив - внешний_цикл - 1; внутренний_цикл++)
        {
            if (числовой_массив[внутренний_цикл] > числовой_массив[внутренний_цикл + 1])
            {
                /* поменять местами соседние элементы */
                временный_значение = числовой_массив[внутренний_цикл];
                числовой_массив[внутренний_цикл] = числовой_массив[внутренний_цикл + 1];
                числовой_массив[внутренний_цикл + 1] = временный_значение;
                был_обмен = 1;
            }
        }
        /* Если в этих циклах обмена не произошло, массив уже отсортирован. */
        if (был_обмен == 0)
            break;
    }
}

int main()
{
    int числа[] = {64, 34, 25, 12, 22, 11, 90};
    int размер_массив = 7;
    int индекс;

    Сортировка_пузырько́м(числа, размер_массив);

    printf("отсортированный массив: ");
    for (индекс = 0; индекс < размер_массив; индекс++)
    {
        printf("%d ", числа[индекс]);
    }
    printf("\n");
    return 0;
}