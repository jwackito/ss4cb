/**
 * يُجري بحثًا معمقًا (DFS)أوليًا على رسم بياني مُمثَّل بقائمة مجاورة.
 * يُعيد ترتيب زيارة العُقد بدءًا من العقدة عقدة_البداية.

 */
function بحث_بتفضيل_العمق(قائمة_التجاور, الجذر) {
    const الْعُقَد_تمت_زيارتها = new Set();
    const ترتيب_الزيارةترتيب_الزيارة = []

    function يستكشف_العقدة(العقدة_الحالية) {
        الْعُقَد_تمت_زيارتها.add(العقدة_الحالية);
        ترتيب_الزيارةترتيب_الزيارة.push(العقدة_الحالية);

        const مجاور_الْعُقَد = قائمة_التجاور[العقدة_الحالية] || [];
        for (const العقدة_المجاورة of مجاور_الْعُقَد) {
            if (!الْعُقَد_تمت_زيارتها.has(العقدة_المجاورة)) {
                يستكشف_العقدة(العقدة_المجاورة);
            }
        }
    }

    يستكشف_العقدة( الجذر);
    return ترتيب_الزيارةترتيب_الزيارة;
}

const مثال_بيان = {
    0: [1, 2],
    1: [0, 3, 4],
    2: [0, 5],
    3: [1],
    4: [1],
    5: [2]
};

const نتيجة = بحث_بتفضيل_العمق(مثال_بيان, 0);
console.log("DFS الزيارة ترتيب:", نتيجة);
