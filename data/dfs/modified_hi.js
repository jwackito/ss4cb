/**
 * एक एडजेंसी सर्च के तौर पर दिखाए गए ग्राफ़ पर डेप्थ पहले सर्च या डेप्थ पहले सर्च (DFS) करता है।
 * वह क्रम लौटाता है जिसमें रूट से शुरू करके नोड्स जोड़े गए थे।
 */
function डीएफएस_सर्च (adjacency_list, रूट) {
    const विज़िट_किए_गए_नोड्स = new Set();
    const विज़िट_क्रम = [];

    function नोड_पड़ताल(मौजूदा_नोड) {
        विज़िट_किए_गए_नोड्स.add(मौजूदा_नोड);
        विज़िट_क्रम.push(मौजूदा_नोड);

        const पड़ोसी_नोड्स = adjacency_list[मौजूदा_नोड] || [];
        for (const पड़ोसी_नोड of पड़ोसी_नोड्स) {
            if (!विज़िट_किए_गए_नोड्स.has(पड़ोसी_नोड)) {
                नोड_पड़ताल(पड़ोसी_नोड);
            }
        }
    }

    नोड_पड़ताल(रूट);
    return विज़िट_क्रम;
}

const उदाहरण_ग्राफ = {
    0: [1, 2],
    1: [0, 3, 4],
    2: [0, 5],
    3: [1],
    4: [1],
    5: [2]
};

const परिणाम = डीएफएस_सर्च​(उदाहरण_ग्राफ, 0);
console.log("DFS विज़िट क्रम:", परिणाम);
