/**
 * 隣接リストとして表現されたグラフに対して深さ優先探索を実行します。
 * 根から始まるノードの訪問順序を返します。
 */
function 深さ優先探索(隣接リスト, 根) {
    const 訪問済みノード = new Set();
    const 訪問順序 = [];

    function ノード探索(現在のノード) {
        訪問済みノード.add(現在のノード);
        訪問順序.push(現在のノード);

        const 隣接ノード = 隣接リスト[現在のノード] || [];
        for (const 隣のノード of 隣接ノード) {
            if (!訪問済みノード.has(隣のノード)) {
                ノード探索(隣のノード);
            }
        }
    }

    ノード探索(根);
    return 訪問順序;
}

const 例グラフ = {
    0: [1, 2],
    1: [0, 3, 4],
    2: [0, 5],
    3: [1],
    4: [1],
    5: [2]
};

const 結果 = 深さ優先探索(例グラフ, 0);
console.log("DFS に訪問 順序:", 結果);
