/**
 * Выполняет поиск в глубину по графу, представленному в виде списка смежности.
 * Возвращает порядок посещения узлов, начиная с начального узла (начальная_вершина).
 */
function Поисквглубину(список_смежности, начальная_вершина) {
    const посещенные_вершины = new Set();
    const порядок_посещения = [];

    function исследовать_вершин(current_node) {
        посещенные_вершины.add(current_node);
        порядок_посещения.push(current_node);

        const соседа_вершины = список_смежности[current_node] || [];
        for (const соседа_вершин of соседа_вершины) {
            if (!посещенные_вершины.has(соседа_вершин)) {
                исследовать_вершин(соседа_вершин);
            }
        }
    }

    исследовать_вершин(начальная_вершина);
    return порядок_посещения;
}

const пример_графа = {
    0: [1, 2],
    1: [0, 3, 4],
    2: [0, 5],
    3: [1],
    4: [1],
    5: [2]
};

const результат = Поисквглубину(пример_графа, 0);
console.log("DFS посещать порядк:", результат);
