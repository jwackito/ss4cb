/**
 * 对以邻接表形式表示的图执行深度优先搜索。
 * 返回从根开始访问节点的顺序。
 */
function 深度优先搜索算法(邻居列表, 根) {
    const 已访问节点 = new Set();
    const 访问顺序 = [];


    function 遍历节点(当前节点) {
        已访问节点.add(当前节点);
        访问顺序.push(当前节点);

        const 邻居节点列表 = 邻居列表[当前节点] || [];
        for (const 邻居节点 of 邻居节点列表) {
            if (!已访问节点.has(邻居节点)) {
                遍历节点(邻居节点);
            }
        }
    }

    遍历节点(根);
    return 访问顺序;
}

const 例图 = {
    0: [1, 2],
    1: [0, 3, 4],
    2: [0, 5],
    3: [1],
    4: [1],
    5: [2]
};

const 结果 = 深度优先搜索算法(例图, 0);
console.log("DFS 访问 顺序:", 结果);
