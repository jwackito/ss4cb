def غربال_إراتوستينس(الحد):
    """
    غربال إراتوستينس هي خوارزمية بسيطة لإيجاد جميع الأعداد الأولية حتى عدد ما.

    """
    أولي = [True] * (الحد + 1)
    أولي[0] = False
    أولي[1] = False

    عدد_الحالية = 2
    while عدد_الحالية * عدد_الحالية <= الحد:
        if أولي[عدد_الحالية]:
            متعدد = عدد_الحالية * عدد_الحالية
            while متعدد <= الحد:
                أولي[متعدد] = False
                متعدد += عدد_الحالية
        عدد_الحالية += 1

    قائمة_أولي = [رقم for رقم in range(2, الحد + 1) if أولي[رقم]]
    return قائمة_أولي


