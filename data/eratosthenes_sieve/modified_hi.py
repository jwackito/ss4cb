def एराटोस्थनीज_की_छलनी(सीमा):
    """
    एराटोस्थनीज की छलनी सबसे सरल अभाज्य संख्या छलनी है। यह 
    एक अभाज्य संख्या एल्गोरिथ्म है जो किसी निश्चित सीमा में सभी अभाज्य संख्याओं को खोजता है। कई 
    """
    प्राइम_है = [True] * (सीमा + 1)
    प्राइम_है[0] = False
    प्राइम_है[1] = False

    वर्तमान_संख्या = 2
    while वर्तमान_संख्या * वर्तमान_संख्या <= सीमा:
        if प्राइम_है[वर्तमान_संख्या]:
            गुणज = वर्तमान_संख्या * वर्तमान_संख्या
            while गुणज <= सीमा:
                प्राइम_है[गुणज] = False
                गुणज += वर्तमान_संख्या
        वर्तमान_संख्या += 1

    अभाज्य_सूची = [संख्या for संख्या in range(2, सीमा + 1) if प्राइम_है[संख्या]]
    return अभाज्य_सूची


if __name__ == "__main__":
    अधिकतम_सीमा = 50
    परिणाम = एराटोस्थनीज_की_छलनी(अधिकतम_सीमा)
    print(f"{अधिकतम_सीमा} तक की प्राइम संख्याएँ: {परिणाम}")
