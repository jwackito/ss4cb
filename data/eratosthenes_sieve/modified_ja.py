def エラトステネスの篩(上限):
    """
    エラトステネスの篩 (エラトステネスのふるい、英: Sieve of Eratosthenes) 
    は、指定された整数以下の全ての素数を発見するための単純なアルゴリズムである。
    """
    素数である = [True] * (上限 + 1)
    素数である[0] = False
    素数である[1] = False

    現在の数値 = 2
    while 現在の数値 * 現在の数値 <= 上限:
        if 素数である[現在の数値]:
            倍数 = 現在の数値 * 現在の数値
            while 倍数 <= 上限:
                素数である[倍数] = False
                倍数 += 現在の数値
        現在の数値 += 1

    素数リスト = [数値 for 数値 in range(2, 上限 + 1) if 素数である[数値]]
    return 素数リスト


if __name__ == "__main__":
    上限 = 50
    結果 = エラトステネスの篩(上限)
    print(f"{上限}までの素数: {結果}")
