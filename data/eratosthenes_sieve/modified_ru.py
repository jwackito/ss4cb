def решето_эратосфена(лимит):
    """
    Решето́ Эратосфе́на — алгоритм нахождения всех простых чисел до 
    некоторого целого числа n, который приписывают древнегреческому математику Эратосфену Киренскому[
    """
    простое_число = [True] * (лимит + 1)
    простое_число[0] = False
    простое_число[1] = False

    текущее_число = 2
    while текущее_число * текущее_число <= лимит:
        if простое_число[текущее_число]:
            кратное = текущее_число * текущее_число
            while кратное <= лимит:
                простое_число[кратное] = False
                кратное += текущее_число
        текущее_число += 1

    список_простых = [число for число in range(2, лимит + 1) if простое_число[число]]
    return список_простых


