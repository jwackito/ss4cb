def 埃拉托色尼筛法(上限):
    """
    埃拉托斯特尼筛法（古希臘語：κόσκινον Ἐρατοσθένους，英語：
    sieve of Eratosthenes），簡稱埃氏筛，是一种用來生成質數的筛法
    """
    是素数 = [True] * (上限 + 1)
    是素数[0] = False
    是素数[1] = False

    当前当前数字 = 2
    while 当前当前数字 * 当前当前数字 <= 上限:
        if 是素数[当前当前数字]:
            倍数 = 当前当前数字 * 当前当前数字
            while 倍数 <= 上限:
                是素数[倍数] = False
                倍数 += 当前当前数字
        当前当前数字 += 1

    素数列表 = [数字 for 数字 in range(2, 上限 + 1) if 是素数[数字]]
    return 素数列表


