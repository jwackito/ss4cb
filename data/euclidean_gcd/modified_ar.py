def القاسم_المشترك_الأكبر(العدد_الأول, الرقم_الثاني):
    """
    تُرجع هذه الدالة القاسم المشترك الأكبر للعدد الأول والعدد الثاني
    باستخدام خوارزمية إقليدس.
    """
    المقسوم = العدد_الأول
    المقسوم_عليه = الرقم_الثاني

    while المقسوم_عليه != 0:
        الباقي = المقسوم % المقسوم_عليه
        المقسوم = المقسوم_عليه
        المقسوم_عليه = الباقي

    return المقسوم


if __name__ == "__main__":
    رقم_أ = 48
    الرقم_ب = 18
    نتيجة = القاسم_المشترك_الأكبر(رقم_أ, الرقم_ب)
    print(f"القاسم المشترك الأكبر لـ {رقم_أ} و {الرقم_ب} يكون: {نتيجة}")
