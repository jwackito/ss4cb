def महत्तम_समापवर्तक(पहला_नंबर, दूसरा_नंबर):
    """
    पहला_नंबर और दूसरा_नंबर का महत्तम समापवर्तक लौटाता है
    यूक्लिडियन एल्गोरिदम का उपयोग करके।

    """
    भाज्य = पहला_नंबर
    भाजक = दूसरा_नंबर

    while भाजक != 0:
        शेषफल = भाज्य % भाजक
        भाज्य = भाजक
        भाजक = शेषफल

    return भाज्य


if __name__ == "__main__":
    संख्या_ए = 48
    संख्या_बी = 18
    परिणाम = महत्तम_समापवर्तक(संख्या_ए, संख्या_बी)
    print(f"{संख्या_ए} और {संख्या_बी} का एल्गोरिदम है: {परिणाम}")
