def ユークリッドの互除法(数1, 数2):
    """
    ユークリッドの互除法を使用して、数1と数2の。
    最大公約数（GCD）を返します。
    """
    被除数 = 数1
    除数 = 数2

    while 除数 != 0:
        余り = 被除数 % 除数
        被除数 = 除数
        除数 = 余り

    return 被除数


