def 欧几里得最大公约数(参数1, 数字2):
    """
    返回 参数1 和 数字2 的最大公约数
    使用欧几里得算法。
    """
    被除数 = 参数1
    除数 = 数字2

    while 除数 != 0:
        余数 = 被除数 % 除数
        被除数 = 除数
        除数 = 余数

    return 被除数


