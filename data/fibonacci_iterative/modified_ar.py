def فيبوناتشي_التكراري(عدد_الحدود):
    """
    في الرياضيات، متتالية فيبوناتشي أو أعداد فيبوناتشي 
    (بالإنجليزية: Fibonacci numbers) نسبة إلى عالم الرياضيات الإيطالي ليوناردو فيبوناتشي، هي متتالية يساوي فيها
    الحد مجموع الحدين السابقين. 
    """
    if عدد_الحدود <= 0:
        return []
    if عدد_الحدود == 1:
        return [0]

    تسلسل = [0, 1]
    while len(تسلسل) < عدد_الحدود:
        الحد_التالي = تسلسل[-1] + تسلسل[-2]
        تسلسل.append(الحد_التالي)

    return تسلسل


if __name__ == "__main__":
    العد = 10
    النتيجة = فيبوناتشي_التكراري(العد)
    print(f"الأول {العد} أرقام فيبوناتشي: {النتيجة}")
