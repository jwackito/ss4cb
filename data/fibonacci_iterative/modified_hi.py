def फाइबोनैचि_पुनरावृत्ति(पदों_की_संख्या):
    """
    गणित में फिबोनाची अनुक्रम एक अनुक्रम है जिसमें प्रत्येक 
    संख्या अपने से पहले वाली दो संख्याओं का योग होती है। 
    """
    if पदों_की_संख्या <= 0:
        return []
    if पदों_की_संख्या == 1:
        return [0]

    अनुक्रम = [0, 1]
    while len(अनुक्रम) < पदों_की_संख्या:
        अगला_पद = अनुक्रम[-1] + अनुक्रम[-2]
        अनुक्रम.append(अगला_पद)

    return अनुक्रम


if __name__ == "__main__":
    संख्या = 10
    परिणाम = फाइबोनैचि_पुनरावृत्ति(संख्या)
    print(f"पहली {संख्या} फिबोनाची संख्याएँ: {परिणाम}")
