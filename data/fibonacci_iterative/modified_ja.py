def フィボナッチ反復法(項の数):
    """
    項の数の最初の数値を含むリストを返します
    反復法法を用いたフィボナッチ数列の計算。
    """
    if 項の数 <= 0:
        return []
    if 項の数 == 1:
        return [0]

    数列 = [0, 1]
    while len(数列) < 項の数:
        次の項 = 数列[-1] + 数列[-2]
        数列.append(次の項)

    return 数列


