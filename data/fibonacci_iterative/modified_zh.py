def 斐波那契迭代(项数):
    """
    返回包含前 1 个 项数 编号的列表
    利用迭代法求解斐波那契数列。
    """
    if 项数 <= 0:
        return []
    if 项数 == 1:
        return [0]

    数列 = [0, 1]
    while len(数列) < 项数:
        下一项 = 数列[-1] + 数列[-2]
        数列.append(下一项)

    return 数列


if __name__ == "__main__":
    数量 = 10
    结果 = 斐波那契迭代(数量)
    print(f"斐波那契数列的前 {数量} 个数字: {结果}")
