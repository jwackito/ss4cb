def البحث_الخطي(قائمة_العناصر, القيمة_المستهدفة):
    """
يبحث عن القيمة المستهدفة في قائمة العناصر بفحص كل عنصر على حدة.   
يعيد فهرس القيمة المستهدفة في حال العثور عليها، أو -1 في حال عدم وجودها.
    """
    الفهرس_الحالي = 0
    while الفهرس_الحالي < len(قائمة_العناصر):
        العنصر_الحالي = قائمة_العناصر[الفهرس_الحالي]
        if العنصر_الحالي == القيمة_المستهدفة:
            return الفهرس_الحالي
        الفهرس_الحالي += 1
    return -1


if __name__ == "__main__":
    أرقام = [34, 7, 23, 32, 5, 62, 14, 89]
    هدف = 32
    فِهرِس = البحث_الخطي(أرقام, هدف)
    if فِهرِس != -1:
        print(f"قيمة {هدف} موجود في فِهرِس {فِهرِس}")
    else:
        print(f"قيمة {هدف} غير موجود")
