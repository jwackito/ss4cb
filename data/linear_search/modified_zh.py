def 线性搜索(元素列表, 目标值):
    """
    通过依次检查物品列表中的每个元素来查找目标值。
    如果找到目标值，则返回其索引；如果不存在，则返回 -1。
    """
    当前索引 = 0
    while 当前索引 < len(元素列表):
        当前元素 = 元素列表[当前索引]
        if 当前元素 == 目标值:
            return 当前索引
        当前索引 += 1
    return -1


if __name__ == "__main__":
    数字 = [34, 7, 23, 32, 5, 62, 14, 89]
    目标 = 32
    索引 = 线性搜索(数字, 目标)
    if 索引 != -1:
        print(f"值 {目标} 处找到索引 {索引}")
    else:
        print(f"值 {目标} 未找到")
