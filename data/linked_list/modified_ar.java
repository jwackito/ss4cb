class قائمةمرتبطة {

    private static class عقدةالقائمة {
        int قيمة_العقدة;
        عقدةالقائمة العقدة_التالية;

        عقدةالقائمة(int قيمة_العقدة) {
            this.قيمة_العقدة = قيمة_العقدة;
            this.العقدة_التالية = null;
        }
    }

    private عقدةالقائمة رأس_القائمة;

    /** يُدرج عقدة جديدة بقيمة في نهاية القائمة */
    public void إضافة_في_النهاية(int قيمة_جديدة) {
        عقدةالقائمة عقدة_جديدة = new عقدةالقائمة(قيمة_جديدة);
        if (رأس_القائمة == null) {
            رأس_القائمة = عقدة_جديدة;
            return;
        }
        عقدةالقائمة العقدة_الحالية = رأس_القائمة;
        while (العقدة_الحالية.العقدة_التالية != null) {
            العقدة_الحالية = العقدة_الحالية.العقدة_التالية;
        }
        العقدة_الحالية.العقدة_التالية = عقدة_جديدة;
    }

    /** يحذف أول عقدة يتم العثور عليها بالقيمة المستهدفة المحددة */
    public void حذفحسبالقيمة(int القيمة_المستهدفة) {
        if (رأس_القائمة == null)
            return;
        if (رأس_القائمة.قيمة_العقدة == القيمة_المستهدفة) {
            رأس_القائمة = رأس_القائمة.العقدة_التالية;
            return;
        }
        عقدةالقائمة العقدة_الحالية = رأس_القائمة;
        while (العقدة_الحالية.العقدة_التالية != null) {
            if (العقدة_الحالية.العقدة_التالية.قيمة_العقدة == القيمة_المستهدفة) {
                العقدة_الحالية.العقدة_التالية = العقدة_الحالية.العقدة_التالية.العقدة_التالية;
                return;
            }
            العقدة_الحالية = العقدة_الحالية.العقدة_التالية;
        }
    }

    /** يطبع جميع قيم العقدة من البداية إلى النهاية */
    public void قائمةطباعة() {

        عقدةالقائمة العقدة_الحالية = رأس_القائمة;
        while (العقدة_الحالية != null) {
            System.out.print(العقدة_الحالية.قيمة_العقدة + " -> ");
            العقدة_الحالية = العقدة_الحالية.العقدة_التالية;
        }
        System.out.println("null");
    }

    public static void main(String[] args) {
        قائمةمرتبطة قائمة_الأرقام = new قائمةمرتبطة();
        قائمة_الأرقام.إضافة_في_النهاية(10);
        قائمة_الأرقام.إضافة_في_النهاية(20);
        قائمة_الأرقام.إضافة_في_النهاية(30);
        قائمة_الأرقام.إضافة_في_النهاية(40);
        System.out.print("قائمة: ");
        قائمة_الأرقام.قائمةطباعة();
        قائمة_الأرقام.حذفحسبالقيمة(20);
        System.out.print("بعد حذف 20: ");
        قائمة_الأرقام.قائمةطباعة();
    }
}
