class लिंक्डलिस्ट {

    private static class लिस्टनोड {
        int नोडमान;
        लिस्टनोड अगलानोड;

        लिस्टनोड(int नोडमान) {
            this.नोडमान = नोडमान;
            this.अगलानोड = null;
        }
    }

    private लिस्टनोड हेडनोड;

    /** लिस्ट के आखिर में वैल्यू के साथ एक नया नोड डालता है */
    public void अंतमेंडालें(int नयामूल्य) {
        लिस्टनोड नयानोड = new लिस्टनोड(नयामूल्य);
        if (हेडनोड == null) {
            हेडनोड = नयानोड;
            return;
        }
        लिस्टनोड वर्तमाननोड = हेडनोड;
        while (वर्तमाननोड.अगलानोड != null) {
            वर्तमाननोड = वर्तमाननोड.अगलानोड;
        }
        वर्तमाननोड.अगलानोड = नयानोड;
    }

    /** दिए गए लक्ष्यमूल्य के साथ मिले पहले नोड को डिलीट करता है */
    public void मानकेअनुसारहटाएँ(int लक्ष्यमूल्य) {
        if (हेडनोड == null)
            return;
        if (हेडनोड.नोडमान == लक्ष्यमूल्य) {
            हेडनोड = हेडनोड.अगलानोड;
            return;
        }
        लिस्टनोड वर्तमाननोड = हेडनोड;
        while (वर्तमाननोड.अगलानोड != null) {
            if (वर्तमाननोड.अगलानोड.नोडमान == लक्ष्यमूल्य) {
                वर्तमाननोड.अगलानोड = वर्तमाननोड.अगलानोड.अगलानोड;
                return;
            }
            वर्तमाननोड = वर्तमाननोड.अगलानोड;
        }
    }

    /** सभी नोड वैल्यू को हेड से टेल तक प्रिंट करता है */
    public void प्रिंटसूची() {
        लिस्टनोड वर्तमाननोड = हेडनोड;
        while (वर्तमाननोड != null) {
            System.out.print(वर्तमाननोड.नोडमान + " -> ");
            वर्तमाननोड = वर्तमाननोड.अगलानोड;
        }
        System.out.println("null");
    }

    public static void main(String[] args) {
        लिंक्डलिस्ट संख्यासूची = new लिंक्डलिस्ट();
        संख्यासूची.अंतमेंडालें(10);
        संख्यासूची.अंतमेंडालें(20);
        संख्यासूची.अंतमेंडालें(30);
        संख्यासूची.अंतमेंडालें(40);
        System.out.print("सूची: ");
        संख्यासूची.प्रिंटसूची();
        संख्यासूची.मानकेअनुसारहटाएँ(20);
        System.out.print("20 हटाने के बाद: ");
        संख्यासूची.प्रिंटसूची();
    }
}
