class 連結リスト {

    private static class リストノード {
        int ノード値;
        リストノード 次ポインタ;

        リストノード(int ノード値) {
            this.ノード値 = ノード値;
            this.次ポインタ = null;
        }
    }

    private リストノード 先頭ノード;

    /** リストの末尾に値を持つ新しいノードを挿入します */
    public void 末尾に挿入(int 新しい値) {
        リストノード 新しいノード = new リストノード(新しい値);
        if (先頭ノード == null) {
            先頭ノード = 新しいノード;
            return;
        }
        リストノード 現在のノード = 先頭ノード;
        while (現在のノード.次ポインタ != null) {
            現在のノード = 現在のノード.次ポインタ;
        }
        現在のノード.次ポインタ = 新しいノード;
    }

    /** 指定された ターゲット値 を持つ最初のノードを削除します */
    public void 値による削除(int ターゲット値) {
        if (先頭ノード == null)
            return;
        if (先頭ノード.ノード値 == ターゲット値) {
            先頭ノード = 先頭ノード.次ポインタ;
            return;
        }
        リストノード 現在のノード = 先頭ノード;
        while (現在のノード.次ポインタ != null) {
            if (現在のノード.次ポインタ.ノード値 == ターゲット値) {
                現在のノード.次ポインタ = 現在のノード.次ポインタ.次ポインタ;
                return;
            }
            現在のノード = 現在のノード.次ポインタ;
        }
    }

    /** 先頭から末尾まで全てのノード値を出力します */
    public void リストを表示() {
        リストノード 現在のノード = 先頭ノード;
        while (現在のノード != null) {
            System.out.print(現在のノード.ノード値 + " -> ");
            現在のノード = 現在のノード.次ポインタ;
        }
        System.out.println("null");
    }

    public static void main(String[] args) {
        連結リスト 番号リスト = new 連結リスト();
        番号リスト.末尾に挿入(10);
        番号リスト.末尾に挿入(20);
        番号リスト.末尾に挿入(30);
        番号リスト.末尾に挿入(40);
        System.out.print("リスト: ");
        番号リスト.リストを表示();
        番号リスト.値による削除(20);
        System.out.print("20を削除した後: ");
        番号リスト.リストを表示();
    }
}
