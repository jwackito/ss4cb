class 链表 {

    private static class 列表节点 {
        int 节点值;
        列表节点 下一个节点;

        列表节点(int 节点值) {
            this.节点值 = 节点值;
            this.下一个节点 = null;
        }
    }

    private 列表节点 头节点;

    /** 在列表末尾插入一个带有特定值的新节点 */
    public void 在末尾插入(int 新值) {
        列表节点 新节点 = new 列表节点(新值);
        if (头节点 == null) {
            头节点 = 新节点;
            return;
        }
        列表节点 当前节点 = 头节点;
        while (当前节点.下一个节点 != null) {
            当前节点 = 当前节点.下一个节点;
        }
        当前节点.下一个节点 = 新节点;
    }

    /** 删除找到的第一个具有给定目标值的节点 */
    public void 按值删除(int 目标值) {
        if (头节点 == null)
            return;
        if (头节点.节点值 == 目标值) {
            头节点 = 头节点.下一个节点;
            return;
        }
        列表节点 当前节点 = 头节点;
        while (当前节点.下一个节点 != null) {
            if (当前节点.下一个节点.节点值 == 目标值) {
                当前节点.下一个节点 = 当前节点.下一个节点.下一个节点;
                return;
            }
            当前节点 = 当前节点.下一个节点;
        }
    }

    /** 从节点头到节点尾打印所有节点值 */
    public void 打印列表() {
        列表节点 当前节点 = 头节点;
        while (当前节点 != null) {
            System.out.print(当前节点.节点值 + " -> ");
            当前节点 = 当前节点.下一个节点;
        }
        System.out.println("null");
    }

    public static void main(String[] args) {
        链表 数字列表 = new 链表();
        数字列表.在末尾插入(10);
        数字列表.在末尾插入(20);
        数字列表.在末尾插入(30);
        数字列表.在末尾插入(40);
        System.out.print("列表: ");
        数字列表.打印列表();
        数字列表.按值删除(20);
        System.out.print("删除 20 后: ");
        数字列表.打印列表();
    }
}
