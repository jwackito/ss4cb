#include <stdio.h>
#include <stdlib.h>

void دمج_النصفين(int مصفوفة_الأرقام[], int الفهرس_الأيسر, int الفهرس_الأوسط, int الفهرس_الأيمن)
{
    int حجم_اليسار = الفهرس_الأوسط - الفهرس_الأيسر + 1;
    int حجم_اليمين = الفهرس_الأيمن - الفهرس_الأوسط;
    int موقع_اليسار, موقع_اليمين, الموضع_المدمج;

    int *مؤقت_الأيسر = (int *)malloc(حجم_اليسار * sizeof(int));
    int *مؤقت_اليمين = (int *)malloc(حجم_اليمين * sizeof(int));

    /* انسخ البيانات إلى مصفوفات مؤقتة */
    for (موقع_اليسار = 0; موقع_اليسار < حجم_اليسار; موقع_اليسار++)
        مؤقت_الأيسر[موقع_اليسار] = مصفوفة_الأرقام[الفهرس_الأيسر + موقع_اليسار];
    for (موقع_اليمين = 0; موقع_اليمين < حجم_اليمين; موقع_اليمين++)
        مؤقت_اليمين[موقع_اليمين] = مصفوفة_الأرقام[الفهرس_الأوسط + 1 + موقع_اليمين];

    موقع_اليسار = 0;
    موقع_اليمين = 0;
    الموضع_المدمج = الفهرس_الأيسر;

    /* ادمج النصفين مرة أخرى في المصفوفة الأصلية */
    while (موقع_اليسار < حجم_اليسار && موقع_اليمين < حجم_اليمين)
    {
        if (مؤقت_الأيسر[موقع_اليسار] <= مؤقت_اليمين[موقع_اليمين])
        {
            مصفوفة_الأرقام[الموضع_المدمج] = مؤقت_الأيسر[موقع_اليسار];
            موقع_اليسار++;
        }
        else
        {
            مصفوفة_الأرقام[الموضع_المدمج] = مؤقت_اليمين[موقع_اليمين];
            موقع_اليمين++;
        }
        الموضع_المدمج++;
    }

    while (موقع_اليسار < حجم_اليسار)
    {
        مصفوفة_الأرقام[الموضع_المدمج] = مؤقت_الأيسر[موقع_اليسار];
        موقع_اليسار++;
        الموضع_المدمج++;
    }
    while (موقع_اليمين < حجم_اليمين)
    {
        مصفوفة_الأرقام[الموضع_المدمج] = مؤقت_اليمين[موقع_اليمين];
        موقع_اليمين++;
        الموضع_المدمج++;
    }

    free(مؤقت_الأيسر);
    free(مؤقت_اليمين);
}

void فرز_الدمج(int مصفوفة_الأرقام[], int الفهرس_الأيسر, int الفهرس_الأيمن)
{
    if (الفهرس_الأيسر < الفهرس_الأيمن)
    {
        int الفهرس_الأوسط = الفهرس_الأيسر + (الفهرس_الأيمن - الفهرس_الأيسر) / 2;
        فرز_الدمج(مصفوفة_الأرقام, الفهرس_الأيسر, الفهرس_الأوسط);
        فرز_الدمج(مصفوفة_الأرقام, الفهرس_الأوسط + 1, الفهرس_الأيمن);
        دمج_النصفين(مصفوفة_الأرقام, الفهرس_الأيسر, الفهرس_الأوسط, الفهرس_الأيمن);
    }
}

int main()
{
    int أرقام[] = {38, 27, 43, 3, 9, 82, 10};
    int طول_المصفوفة = 7;
    int فهرس;

    فرز_الدمج(أرقام, 0, طول_المصفوفة - 1);

    printf("مصفوفة مرتبة: ");
    for (فهرس = 0; فهرس < طول_المصفوفة; فهرس++)
        printf("%d ", أرقام[فهرس]);
    printf("\n");
    return 0;
}
