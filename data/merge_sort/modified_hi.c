#include <stdio.h>
#include <stdlib.h>

void आधों_को_जोड़ें(int संख्या_सरणी[], int बायाँ_सूचकांक, int मध्य_सूचकांक, int दायां_सूचकांक)
{
    int बायाँ_आकार = मध्य_सूचकांक - बायाँ_सूचकांक + 1;
    int दायां_आकार = दायां_सूचकांक - मध्य_सूचकांक;
    int बायाँ_स्थिति, दायां_स्थिति, मर्ज_स्थिति;

    int *बायाँ_अस्थायी = (int *)malloc(बायाँ_आकार * sizeof(int));
    int *दायां_अस्थायी = (int *)malloc(दायां_आकार * sizeof(int));

    /* डेटा को टेम्पररी ऐरे में कॉपी करें */
    for (बायाँ_स्थिति = 0; बायाँ_स्थिति < बायाँ_आकार; बायाँ_स्थिति++)
        बायाँ_अस्थायी[बायाँ_स्थिति] = संख्या_सरणी[बायाँ_सूचकांक + बायाँ_स्थिति];
    for (दायां_स्थिति = 0; दायां_स्थिति < दायां_आकार; दायां_स्थिति++)
        दायां_अस्थायी[दायां_स्थिति] = संख्या_सरणी[मध्य_सूचकांक + 1 + दायां_स्थिति];

    बायाँ_स्थिति = 0;
    दायां_स्थिति = 0;
    मर्ज_स्थिति = बायाँ_सूचकांक;

    /* दोनों हिस्सों को वापस ओरिजिनल सरणी में मर्ज करें */
    while (बायाँ_स्थिति < बायाँ_आकार && दायां_स्थिति < दायां_आकार)
    {
        if (बायाँ_अस्थायी[बायाँ_स्थिति] <= दायां_अस्थायी[दायां_स्थिति])
        {
            संख्या_सरणी[मर्ज_स्थिति] = बायाँ_अस्थायी[बायाँ_स्थिति];
            बायाँ_स्थिति++;
        }
        else
        {
            संख्या_सरणी[मर्ज_स्थिति] = दायां_अस्थायी[दायां_स्थिति];
            दायां_स्थिति++;
        }
        मर्ज_स्थिति++;
    }

    while (बायाँ_स्थिति < बायाँ_आकार)
    {
        संख्या_सरणी[मर्ज_स्थिति] = बायाँ_अस्थायी[बायाँ_स्थिति];
        बायाँ_स्थिति++;
        मर्ज_स्थिति++;
    }
    while (दायां_स्थिति < दायां_आकार)
    {
        संख्या_सरणी[मर्ज_स्थिति] = दायां_अस्थायी[दायां_स्थिति];
        दायां_स्थिति++;
        मर्ज_स्थिति++;
    }

    free(बायाँ_अस्थायी);
    free(दायां_अस्थायी);
}

void मर्ज_सॉर्ट(int संख्या_सरणी[], int बायाँ_सूचकांक, int दायां_सूचकांक)
{
    if (बायाँ_सूचकांक < दायां_सूचकांक)
    {
        int मध्य_सूचकांक = बायाँ_सूचकांक + (दायां_सूचकांक - बायाँ_सूचकांक) / 2;
        मर्ज_सॉर्ट(संख्या_सरणी, बायाँ_सूचकांक, मध्य_सूचकांक);
        मर्ज_सॉर्ट(संख्या_सरणी, मध्य_सूचकांक + 1, दायां_सूचकांक);
        आधों_को_जोड़ें(संख्या_सरणी, बायाँ_सूचकांक, मध्य_सूचकांक, दायां_सूचकांक);
    }
}

int main()
{
    int नंबर[] = {38, 27, 43, 3, 9, 82, 10};
    int सरणी_लंबाई = 7;
    int सूचकांक;

    मर्ज_सॉर्ट(नंबर, 0, सरणी_लंबाई - 1);

    printf("क्रमबद्ध सरणी: ");
    for (सूचकांक = 0; सूचकांक < सरणी_लंबाई; सूचकांक++)
        printf("%d ", नंबर[सूचकांक]);
    printf("\n");
    return 0;
}
