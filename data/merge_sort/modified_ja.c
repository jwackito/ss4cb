#include <stdio.h>
#include <stdlib.h>

void ハーフマージ(int 数値配列[], int 左インデックス, int ミドルインデックス, int 右インデックス)
{
    int 左サイズ = ミドルインデックス - 左インデックス + 1;
    int 右サイズ = 右インデックス - ミドルインデックス;
    int 左位置, 右位置, マージされた位置;

    int *左一時的 = (int *)malloc(左サイズ * sizeof(int));
    int *右一時的 = (int *)malloc(右サイズ * sizeof(int));

    /* データを一時配列にコピーする */
    for (左位置 = 0; 左位置 < 左サイズ; 左位置++)
        左一時的[左位置] = 数値配列[左インデックス + 左位置];
    for (右位置 = 0; 右位置 < 右サイズ; 右位置++)
        右一時的[右位置] = 数値配列[ミドルインデックス + 1 + 右位置];

    左位置 = 0;
    右位置 = 0;
    マージされた位置 = 左インデックス;

    /* 2つの半分を元の配列にマージします */
    while (左位置 < 左サイズ && 右位置 < 右サイズ)
    {
        if (左一時的[左位置] <= 右一時的[右位置])
        {
            数値配列[マージされた位置] = 左一時的[左位置];
            左位置++;
        }
        else
        {
            数値配列[マージされた位置] = 右一時的[右位置];
            右位置++;
        }
        マージされた位置++;
    }

    while (左位置 < 左サイズ)
    {
        数値配列[マージされた位置] = 左一時的[左位置];
        左位置++;
        マージされた位置++;
    }
    while (右位置 < 右サイズ)
    {
        数値配列[マージされた位置] = 右一時的[右位置];
        右位置++;
        マージされた位置++;
    }

    free(左一時的);
    free(右一時的);
}

void マージソート(int 数値配列[], int 左インデックス, int 右インデックス)
{
    if (左インデックス < 右インデックス)
    {
        int ミドルインデックス = 左インデックス + (右インデックス - 左インデックス) / 2;
        マージソート(数値配列, 左インデックス, ミドルインデックス);
        マージソート(数値配列, ミドルインデックス + 1, 右インデックス);
        ハーフマージ(数値配列, 左インデックス, ミドルインデックス, 右インデックス);
    }
}

int main()
{
    int 数字[] = {38, 27, 43, 3, 9, 82, 10};
    int 配列の長さ = 7;
    int 索引;

    マージソート(数字, 0, 配列の長さ - 1);

    printf("ソート済み配列: ");
    for (索引 = 0; 索引 < 配列の長さ; 索引++)
        printf("%d ", 数字[索引]);
    printf("\n");
    return 0;
}
