#include <stdio.h>
#include <stdlib.h>

void 归并(int 数字数组[], int 左索引, int 中间索引, int 右索引)
{
    int 左侧大小 = 中间索引 - 左索引 + 1;
    int 向右大小 = 右索引 - 中间索引;
    int 左索引, 向右位置, 合并位置;

    int *左临时 = (int *)malloc(左侧大小 * sizeof(int));
    int *向右临时 = (int *)malloc(向右大小 * sizeof(int));

    /* 将数据复制到临时数组中 */
    for (左索引 = 0; 左索引 < 左侧大小; 左索引++)
        左临时[左索引] = 数字数组[左索引 + 左索引];
    for (向右位置 = 0; 向右位置 < 向右大小; 向右位置++)
        向右临时[向右位置] = 数字数组[中间索引 + 1 + 向右位置];

    左索引 = 0;
    向右位置 = 0;
    合并位置 = 左索引;

    /* 将两半合并回原始数组 */
    while (左索引 < 左侧大小 && 向右位置 < 向右大小)
    {
        if (左临时[左索引] <= 向右临时[向右位置])
        {
            数字数组[合并位置] = 左临时[左索引];
            左索引++;
        }
        else
        {
            数字数组[合并位置] = 向右临时[向右位置];
            向右位置++;
        }
        合并位置++;
    }

    while (左索引 < 左侧大小)
    {
        数字数组[合并位置] = 左临时[左索引];
        左索引++;
        合并位置++;
    }
    while (向右位置 < 向右大小)
    {
        数字数组[合并位置] = 向右临时[向右位置];
        向右位置++;
        合并位置++;
    }

    free(左临时);
    free(向右临时);
}

void 归并排序(int 数字数组[], int 左索引, int 右索引)
{
    if (左索引 < 右索引)
    {
        int 中间索引 = 左索引 + (右索引 - 左索引) / 2;
        归并排序(数字数组, 左索引, 中间索引);
        归并排序(数字数组, 中间索引 + 1, 右索引);
        归并(数字数组, 左索引, 中间索引, 右索引);
    }
}

int main()
{
    int 数字[] = {38, 27, 43, 3, 9, 82, 10};
    int 数组长度 = 7;
    int 索引;

    归并排序(数字, 0, 数组长度 - 1);

    printf("已排序数组: ");
    for (索引 = 0; 索引 < 数组长度; 索引++)
        printf("%d ", 数字[索引]);
    printf("\n");
    return 0;
}
