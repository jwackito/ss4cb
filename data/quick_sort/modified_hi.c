#include <stdio.h>

void स्वैप_तत्व(int *प्रथम_तत्व, int *दूसरा_तत्व)
{
    int अस्थायी_मूल्य = *प्रथम_तत्व;
    *प्रथम_तत्व = *दूसरा_तत्व;
    *दूसरा_तत्व = अस्थायी_मूल्य;
}

int विभाजन_सरणी(int संख्या_सरणी[], int निम्न_सूचकांक, int उच्च_सूचकांक)
{
    int पिवट_मान = संख्या_सरणी[उच्च_सूचकांक];
    int छोटे_तत्व_सूचकांक = निम्न_सूचकांक - 1;
    int वर्तमान_सूचकांक;

    for (वर्तमान_सूचकांक = निम्न_सूचकांक; वर्तमान_सूचकांक < उच्च_सूचकांक; वर्तमान_सूचकांक++)
    {
        if (संख्या_सरणी[वर्तमान_सूचकांक] <= पिवट_मान)
        {
            छोटे_तत्व_सूचकांक++;
            स्वैप_तत्व(&संख्या_सरणी[छोटे_तत्व_सूचकांक], &संख्या_सरणी[वर्तमान_सूचकांक]);
        }
    }
    स्वैप_तत्व(&संख्या_सरणी[छोटे_तत्व_सूचकांक + 1], &संख्या_सरणी[उच्च_सूचकांक]);
    return छोटे_तत्व_सूचकांक + 1;
}

void quick_sort(int संख्या_सरणी[], int निम्न_सूचकांक, int उच्च_सूचकांक)
{
    if (निम्न_सूचकांक < उच्च_सूचकांक)
    {
        int पिवट_सूचकांक = विभाजन_सरणी(संख्या_सरणी, निम्न_सूचकांक, उच्च_सूचकांक);
        quick_sort(संख्या_सरणी, निम्न_सूचकांक, पिवट_सूचकांक - 1);
        quick_sort(संख्या_सरणी, पिवट_सूचकांक + 1, उच्च_सूचकांक);
    }
}

int main()
{
    int नंबर[] = {10, 7, 8, 9, 1, 5};
    int सरणी_लंबाई = 6;
    int अनुक्रमणिका;

    quick_sort(नंबर, 0, सरणी_लंबाई - 1);

    printf("क्रमबद्ध सरणी: ");
    for (अनुक्रमणिका = 0; अनुक्रमणिका < सरणी_लंबाई; अनुक्रमणिका++)
        printf("%d ", नंबर[अनुक्रमणिका]);
    printf("\n");
    return 0;
}
