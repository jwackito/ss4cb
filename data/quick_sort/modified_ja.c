#include <stdio.h>

void 要素の交換(int *最初の要素, int *第二の要素)
{
    int 一時的な値 = *最初の要素;
    *最初の要素 = *第二の要素;
    *第二の要素 = 一時的な値;
}

int パーティション分割(int 数値の配列[], int 低インデックス, int 高インデックス)
{
    int ピボット値 = 数値の配列[高インデックス];
    int 小要素のインデックス = 低インデックス - 1;
    int 現在のインデックス;

    for (現在のインデックス = 低インデックス; 現在のインデックス < 高インデックス; 現在のインデックス++)
    {
        if (数値の配列[現在のインデックス] <= ピボット値)
        {
            小要素のインデックス++;
            要素の交換(&数値の配列[小要素のインデックス], &数値の配列[現在のインデックス]);
        }
    }
    要素の交換(&数値の配列[小要素のインデックス + 1], &数値の配列[高インデックス]);
    return 小要素のインデックス + 1;
}

void クイックソート(int 数値の配列[], int 低インデックス, int 高インデックス)
{
    if (低インデックス < 高インデックス)
    {
        int ピボットインデックス = パーティション分割(数値の配列, 低インデックス, 高インデックス);
        クイックソート(数値の配列, 低インデックス, ピボットインデックス - 1);
        クイックソート(数値の配列, ピボットインデックス + 1, 高インデックス);
    }
}

int main()
{
    int 数字[] = {10, 7, 8, 9, 1, 5};
    int 数値の配列の長さ = 6;
    int 索引;

    クイックソート(数字, 0, 数値の配列の長さ - 1);

    printf("ソート済み数値の配列: ");
    for (索引 = 0; 索引 < 数値の配列の長さ; 索引++)
        printf("%d ", 数字[索引]);
    printf("\n");
    return 0;
}
