#include <stdio.h>

void 交换元素(int *第一个元素, int *第二个元素)
{
    int 临时值 = *第一个元素;
    *第一个元素 = *第二个元素;
    *第二个元素 = 临时值;
}

int 分区数组(int 数字数组[], int 低索引, int 高索引)
{
    int 枢轴值 = 数字数组[高索引];
    int 较小的元素索引 = 低索引 - 1;
    int 当前索引;

    for (当前索引 = 低索引; 当前索引 < 高索引; 当前索引++)
    {
        if (数字数组[当前索引] <= 枢轴值)
        {
            较小的元素索引++;
            交换元素(&数字数组[较小的元素索引], &数字数组[当前索引]);
        }
    }
    交换元素(&数字数组[较小的元素索引 + 1], &数字数组[高索引]);
    return 较小的元素索引 + 1;
}

void 快速排序(int 数字数组[], int 低索引, int 高索引)
{
    if (低索引 < 高索引)
    {
        int 枢轴索引 = 分区数组(数字数组, 低索引, 高索引);
        快速排序(数字数组, 低索引, 枢轴索引 - 1);
        快速排序(数字数组, 枢轴索引 + 1, 高索引);
    }
}

int main()
{
    int 数字[] = {10, 7, 8, 9, 1, 5};
    int 数组长度 = 6;
    int 索引;

    快速排序(数字, 0, 数组长度 - 1);

    printf("Sorted array: ");
    for (索引 = 0; 索引 < 数组长度; 索引++)
        printf("%d ", 数字[索引]);
    printf("\n");
    return 0;
}
