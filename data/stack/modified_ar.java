class مكدس {
    private int[] مصفوفة_العناصر;
    private int أعلى_المؤشر;
    private int سعة_المكدس;

    /** يقوم بتهيئة مكدس بالسعة المحددة */
    public مكدس(int سعة_المكدس) {
        this.سعة_المكدس = سعة_المكدس;
        this.مصفوفة_العناصر = new int[سعة_المكدس];
        this.أعلى_المؤشر = -1;
    }

    /** تُرجع القيمة "صحيح" إذا لم يكن للمكدس أي عناصر */
    public boolean فارغ() {
        return أعلى_المؤشر == -1;
    }

    /** تُرجع القيمة "صحيح" إذا وصلت المكدسة إلى سعتها القصوى */
    public boolean ممتلئ() {
        return أعلى_المؤشر == سعة_المكدس - 1;
    }

    /** يضيف عنصرًا جديدًا أعلى المكدس */
    public void دفع(int عنصر_جديد) {
        if (ممتلئ()) {
            System.out.println("خطأ: تجاوز سعة المكدس - لا يمكن الدفع" + عنصر_جديد);
            return;
        }
        مصفوفة_العناصر[++أعلى_المؤشر] = عنصر_جديد;
    }

    /** يزيل العنصر العلوي من المكدس ويعيده */
    public int سحب() {
        if (فارغ()) {
            System.out.println("خطأ: المكدس فارغ - لا يمكن السحب");
            return -1;
        }
        return مصفوفة_العناصر[أعلى_المؤشر--];
    }

    /** يعرض العنصر العلوي دون إزالته */
    public int فحص() {
        if (فارغ()) {
            System.out.println("المكدس فارغ");
            return -1;
        }
        return مصفوفة_العناصر[أعلى_المؤشر];
    }

    public static void main(String[] args) {
        مكدس مكدس_الأرقام = new مكدس(5);
        مكدس_الأرقام.دفع(10);
        مكدس_الأرقام.دفع(20);
        مكدس_الأرقام.دفع(30);
        System.out.println("العنصر العلوي: " + مكدس_الأرقام.فحص());
        System.out.println("تم سحب: " + مكدس_الأرقام.سحب());
        System.out.println("تم سحب: " + مكدس_الأرقام.سحب());
        System.out.println("فارغ: " + مكدس_الأرقام.فارغ());
    }
}
