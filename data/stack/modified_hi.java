class ढेर {
    private int[] तत्वसरणी;
    private int शीर्षसूचकांक;
    private int स्टैकक्षमता;

    /** दी गई कैपेसिटी के साथ स्टैक को इनिशियलाइज़ करता है */
    public ढेर(int स्टैकक्षमता) {
        this.स्टैकक्षमता = स्टैकक्षमता;
        this.तत्वसरणी = new int[स्टैकक्षमता];
        this.शीर्षसूचकांक = -1;
    }

    /** अगर स्टैक में कोई एलिमेंट नहीं है तो true रिटर्न करता है */
    public boolean खालीहै() {
        return शीर्षसूचकांक == -1;
    }

    /** अगर स्टैक अपनी कैपेसिटी तक पहुँच गया है तो true रिटर्न करता है */
    public boolean भराहै() {
        return शीर्षसूचकांक == स्टैकक्षमता - 1;
    }

    /** स्टैक के ऊपर एक नया एलिमेंट जोड़ता है */
    public void पुश(int नयातत्व) {
        if (भराहै()) {
            System.out.println("त्रुटि: स्टैक अतिप्रवाह" + नयातत्व);
            return;
        }
        तत्वसरणी[++शीर्षसूचकांक] = नयातत्व;
    }

    /** स्टैक के टॉप एलिमेंट को हटाता है और वापस करता है */
    public int पॉप() {
        if (खालीहै()) {
            System.out.println("त्रुटि: स्टैक अल्पप्रवाह");
            return -1;
        }
        return तत्वसरणी[शीर्षसूचकांक--];
    }

    /** टॉप एलिमेंट को बिना हटाए लौटाता है */
    public int उझकना() {
        if (खालीहै()) {
            System.out.println("स्टैक खाली है");
            return -1;
        }
        return तत्वसरणी[शीर्षसूचकांक];
    }

    public static void main(String[] args) {
        ढेर संख्याढेर = new ढेर(5);
        संख्याढेर.पुश(10);
        संख्याढेर.पुश(20);
        संख्याढेर.पुश(30);
        System.out.println("तत्व शीर्ष: " + संख्याढेर.उझकना());
        System.out.println("बाहर निकाला: " + संख्याढेर.पॉप());
        System.out.println("बाहर निकाला: " + संख्याढेर.पॉप());
        System.out.println("खाली है: " + संख्याढेर.खालीहै());
    }
}
