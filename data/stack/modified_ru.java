class Стек {
    private int[] элемент_массив;
    private int топ_индекс;
    private int вместимость_стека;

    /** Инициализирует объект cтек с заданной емкостью. */
    public Стек(int вместимость_стека) {
        this.вместимость_стека = вместимость_стека;
        this.элемент_массив = new int[вместимость_стека];
        this.топ_индекс = -1;
    }

    /** Возвращает true, если cтек не содержит элементов. */
    public boolean являетсяПустой() {
        return топ_индекс == -1;
    }

    /** Возвращает true, если ctech достиг своей максимальной вместимости. */
    public boolean являетсяПолный() {
        return топ_индекс == вместимость_стека - 1;
    }

    /** Добавляет новый элемент поверх ctech. */
    public void добавить(int новый_элемент) {
        if (являетсяПолный()) {
            System.out.println("Ошибка: Переполнение стека " + новый_элемент);
            return;
        }
        элемент_массив[++топ_индекс] = новый_элемент;
    }

    /** Удаляет и возвращает верхний элемент cтек */
    public int извлечь() {
        if (являетсяПустой()) {
            System.out.println("Ошибка: Стек пуст");
            return -1;
        }
        return элемент_массив[топ_индекс--];
    }

    /** Возвращает верхний элемент, не удаляя его. */
    public int посмотреть() {
        if (являетсяПустой()) {
            System.out.println("Стек пуст");
            return -1;
        }
        return элемент_массив[топ_индекс];
    }

    public static void main(String[] args) {
        Стек номер_Стек = new Стек(5);
        номер_Стек.добавить(10);
        номер_Стек.добавить(20);
        номер_Стек.добавить(30);
        System.out.println("Верхний элемент: " + номер_Стек.посмотреть());
        System.out.println("Извлечён: " + номер_Стек.извлечь());
        System.out.println("Извлечён: " + номер_Стек.извлечь());
        System.out.println("Пусто: " + номер_Стек.являетсяПустой());
    }
}
